package XVI_Eksempler_Fra_ChatGPT;
/*
Skriv et program, som for ligningen y=3*x*x+6*x+9 udskriver værdien af y for x=7.
 */
public class LigningVærdi {
    public static void main(String[] args) {
        // Angiv værdien af x
        double x = 7.0;

        // Beregn værdien af y for den givne x-værdi
        double y = 3 * x * x + 6 * x + 9;

        // Udskriv resultatet
        System.out.println("For x = " + x + ", er y = " + y);
    }
}
/*
Dette program beregner værdien af y ved at bruge den givne x-værdi og derefter udskriver resultatet.
Når du kører programmet, vil du se resultatet for x = 7.
 */