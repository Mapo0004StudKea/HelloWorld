package XVII_Eksempler_Fra_ChatGPT.KEA_Opgaver;
/*
Lav et program, der tæller nedad fra 10 til 1.
 */
public class Nedtælling {
    public static void main(String[] args) {
        for (int i = 10; i >= 1; i--) {
            System.out.println(i);
        }
    }
}
/*
Dette program bruger en for-løkke til at tælle nedad fra 10 til 1 ved at initialisere i med 10,
køre så længe i er større eller lig med 1, og trække 1 fra i i hver iteration. Resultatet er,
at programmet udskriver tallene fra 10 ned til 1.
 */